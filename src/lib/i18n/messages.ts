export type Messages = (typeof messages)['pl'];

/** Rozwiązuje klucz w notacji kropkowej z fallbackiem do PL. */
export function translate(locale: Locale, key: string): string {
  const fromLocale = resolve(messages[locale], key);
  if (fromLocale !== undefined) return fromLocale;
  const fromPl = resolve(messages.pl, key);
  return fromPl !== undefined ? fromPl : key;
}

function resolve(dict: unknown, key: string): string | undefined {
  const node = resolveNode(dict, key);
  return typeof node === 'string' ? node : undefined;
}

/** Rozwiązuje dowolny węzeł (string/array/obiekt) z fallbackiem do PL. */
export function translateRaw(locale: Locale, key: string): unknown {
  const fromLocale = resolveNode(messages[locale], key);
  if (fromLocale !== undefined) return fromLocale;
  return resolveNode(messages.pl, key);
}

function resolveNode(dict: unknown, key: string): unknown {
  const parts = key.split('.');
  let cur: unknown = dict;
  for (const p of parts) {
    if (cur && typeof cur === 'object' && p in (cur as Record<string, unknown>)) {
      cur = (cur as Record<string, unknown>)[p];
    } else {
      return undefined;
    }
  }
  return cur;
}
